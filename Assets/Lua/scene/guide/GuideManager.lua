function GuideManager:clear()
    self.guideNodeMap = {}
end

function GuideManager:onGuideChange()
    for key, value in pairs(self.guideNodeMap) do
        if not PlayerData.guidesMap[key] and not value.isExtra then
            self:removeGuide(key)
        end
    end

    for key, value in pairs(PlayerData.guidesMap) do
        if not self.guideNodeMap[key] then
            self:addGuide(value.guideId)
        end
    end
end

function GuideManager:addGuide(guideId, isExtra)
    self.guideNodeMap[guideId] = ggclass.GuideNode.new(guideId, isExtra)
end

function GuideManager:removeGuide(guideId)
    self.guideNodeMap[guideId]:release()
    self.guideNodeMap[guideId] = nil
end

function GuideManager:Update()
    for key, value in pairs(self.guideNodeMap) do
        value:Update(GuideUtil.checkIsCanGuide(value.guideCfg))
    end
end

function GuideManager:isHardGuiding()
    for key, value in pairs(self.guideNodeMap) do
        if value.guideStage == GuideManager.GUIDE_STAGE_WAITING and value.guideCfg.isSoft ~= 1 then
            return true
        end
    end
    return false
end

function GuideManager:initScreenInfo()
    local canvasScaler = gg.uiManager.uiRoot.normalNode.transform:GetComponent("CanvasScaler")
    local defaultWidth = canvasScaler.referenceResolution.x
    local defaultHeight = canvasScaler.referenceResolution.y
    local newScreenWidth = UnityEngine.Screen.width
    local newScreenHeight = UnityEngine.Screen.height
    local referenceProportion = defaultWidth / defaultHeight
    local newProportion = newScreenWidth / newScreenHeight
    local referenceHeight = 0
    local referenceWidth = 0
    if newProportion >= referenceProportion then
        referenceHeight = defaultHeight
        referenceWidth = newProportion * defaultHeight
    else
        newProportion = newScreenHeight / newScreenWidth
        referenceWidth = defaultWidth
        referenceHeight = newProportion * defaultWidth
    end
    self.biasX = referenceWidth / newScreenWidth
    self.biasY = referenceHeight / newScreenHeight
end

